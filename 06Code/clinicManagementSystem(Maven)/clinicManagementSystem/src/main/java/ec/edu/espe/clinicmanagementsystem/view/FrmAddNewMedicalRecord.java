    private void btnAddRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddRegisterActionPerformed
        int option = 0;
        
        if(!GUIValidation.validateOnlyNumbers(txtPatientId, "Id del paciente")){
            return;    
        }
        if(!GUIValidation.validateOnlyNumbers(txtMedicalHistoryId,"id del nuevo registro de historia")){
            return;
        }
        if(!GUIValidation.validateNotEmpty(txaObservations, "Observaciones")){
            return;
        }
        if(!GUIValidation.validateNotEmpty(txaTreatments, "Tratamientos")){
            return;
        }
        if(!GUIValidation.validateNotEmpty(txtAllergies, "Alergias")){
            return;
        }
        if(!GUIValidation.validateNotEmpty(txtDiseases, "Enfermedades")){
            return;
        }
        if(!GUIValidation.validateDateRange(calDateMedicalRecord, "Fecha del registro")){
            return;
        }
        
        readValues();
        
        option = JOptionPane.showConfirmDialog(rootPane, "Añadiendo nuevo Historial Médico" + medicalHistory , "Añadir nuevo Historial Médico?", JOptionPane.YES_NO_CANCEL_OPTION);
       if (option == JOptionPane.YES_OPTION){
           JOptionPane.showMessageDialog(rootPane, "El historial médico fué registrado" + medicalHistory);
           emptyFields();           
       } else if (option == JOptionPane.NO_OPTION){
           JOptionPane.showMessageDialog(rootPane, "La información de este historial no se guardará ","",JOptionPane.WARNING_MESSAGE);
       } else {
           txtPatientId.requestFocus();
       }  
         
    }//GEN-LAST:event_btnAddRegisterActionPerformed

