    private void emptyFields() {
        txtPatientId.setText("");
        txtpPatientFound.setText("");
        txtMedicalHistoryId.setText("");
        txaObservations.setText("");
        txaTreatments.setText("");
        txtAllergies.setText("");
        txtDiseases.setText("");
        calDateMedicalRecord.setDate(null);
    }
    
    private void readValues() {
        int id = Integer.parseInt(txtPatientId.getText());
        int idMedicalHistory = Integer.parseInt(txtMedicalHistoryId.getText());
        String observations = txaObservations.getText();
        String treatments = txaTreatments.getText();
        String allergies = txtAllergies.getText();
        String diseases = txtDiseases.getText();
        java.util.Date dateSelected = calDateMedicalRecord.getDate();
        
        SimpleDateFormat yearFormat = new SimpleDateFormat("yyyy");
        SimpleDateFormat monthFormat = new SimpleDateFormat("MM");
        SimpleDateFormat dayFormat = new SimpleDateFormat("dd");
        
        int year = Integer.parseInt(yearFormat.format(dateSelected));
        int month = Integer.parseInt(monthFormat.format(dateSelected));
        int day = Integer.parseInt(dayFormat.format(dateSelected));
        
        int hour = 0;
        int minute = 0;
        
        date = new Date(day, month, year, hour, minute);
        
        medicalHistory = new MedicalHistory(idMedicalHistory, date, allergies, diseases, treatments, observations);
  
    }
    
    
